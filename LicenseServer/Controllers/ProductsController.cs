        // POST: Products/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "Id,Name,PrivateKey,PublicKey")] Product product)
        {
            if (ModelState.IsValid)
            {
                
                var keyGenerator = Portable.Licensing.Security.Cryptography.KeyGenerator.Create();
                var keyPair = keyGenerator.GenerateKeyPair();
                var privateKey = keyPair.ToEncryptedPrivateKeyString(ConfigRepository.passPhrase);
                var publicKey = keyPair.ToPublicKeyString();

                product.PublicKey = publicKey;
                product.PrivateKey = privateKey;

                db.Products.Add(product);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            return View(product);
        }

